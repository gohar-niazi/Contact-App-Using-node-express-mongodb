import mongoose from "mongoose";
const contactdata = mongoose.Schema({
  first_name: {
    type: String,
  },
  last_name: {
    type: String,
  },
  email: {
    type: String,
  },
  phone: {
    type: String,
  },
  address: {
    type: String,
  },
});
const contacts = mongoose.model("contacts", contactdata);

export default contacts;
